import mongoose, {Schema} from "mongoose";
import mongooseAggregatePaginate from "mongoose-aggregate-paginate-v2";

const videoSchema = new Schema(
    {
        videoFile: {
            type: String, //cloudinary url
            required: true
        },
        thumbnail: {
            type: String, //cloudinary url
            required: true
        },
        title: {
            type: String, 
            required: true
        },
        description: {
            type: String, 
            required: true
        },
        duration: {
            type: Number, 
            required: true
        },
        views: {
            type: Number,
            default: 0
        },
        isPublished: {
            type: Boolean,
            default: true
        },
        owner: {
            type: Schema.Types.ObjectId,
            ref: "User"
        }

    }, 
    {
        timestamps: true
    }
)
// a hook is a function that is called before or after a certain event occurs in the database.
videoSchema.plugin(mongooseAggregatePaginate)  // this plugin will add the paginate method to the aggregate function of the video model, which will allow us to paginate the results of the aggregate function. This is useful for pagination of videos on the frontend.

export const Video = mongoose.model("Video", videoSchema)